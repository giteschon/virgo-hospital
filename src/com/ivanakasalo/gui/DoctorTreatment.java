    private void fillTableMedicalHistory() {
        if (manager.getItemsPerPatient(Statics.patient) != null) {
            List<Item> items = manager.getItemsPerPatient(Statics.patient);
            Object rowData[] = new Object[5];

            for (int i = 0; i < items.size(); i++) {
                rowData[0] = items.get(i).getEntryDate();
                rowData[1] = items.get(i).getMedicine().getMedicine();
                rowData[2] = items.get(i).getTest().getName();
                rowData[3] = items.get(i).getConsultation().getConsultation();

                GuiUtilities.getModel(tblMedicalHistory).addRow(rowData);
            }

        }

    }


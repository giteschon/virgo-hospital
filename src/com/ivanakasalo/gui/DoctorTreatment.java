    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        Medicine medication = (Medicine) ddlMedication.getSelectedItem();
        Test test = (Test) ddlTest.getSelectedItem();
        Consultation consultation = (Consultation) ddlConsultation.getSelectedItem();

        Item item = new Item(medication, test, consultation, Statics.patient);

        try {
            manager.addItem(item);
            lblSuccess.setText("Item added successfully");
            btnSendToReception.setEnabled(true);
        } catch (Exception e) {
            lblSuccess.setText("Item NOT added successfully");
            lblSuccess.setForeground(Color.red);
        }

        if (txtDateOfNewApp.isEnabled() && !txtDateOfNewApp.getText().equals("")) {
            manager.addFutureAppointement(Statics.patient, Statics.doctor, Utilities.getSqlDate(txtDateOfNewApp.getText()));
        }


    }//GEN-LAST:event_btnAddItemActionPerformed

